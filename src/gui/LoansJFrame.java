    public boolean check() throws IOException {
        File file = new File("Student.dat");
        StudentFile fileS = new StudentFile(file);
        for (int i = 0; i < fileS.getAllStudent().size(); i++) {

            if (fileS.getAllStudent().get(i).getId().equalsIgnoreCase(this.jTextField1.getText())) {
                this.setVisible(true);
                student = fileS.getAllStudent().get(i).getId();
                nameS = fileS.getAllStudent().get(i).getName();
                CheckLoan checkLoans = new CheckLoan(student, nameS);
                BookCode bookCode = new BookCode(student,nameS);
                AudiovisualCode audiovisualCode= new AudiovisualCode(student, nameS);
                checkLoans.setVisible(true);
                this.setVisible(false);
                return true;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Debe de registrarse primero");
        return false;
    }
